    private void awal()
    {
        try {
            List<model.table_gaji> data = gajiService.tampil();
            DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
            table.setRowCount(0);
            for (model.table_gaji row : data)
            {
                table.addRow(new Object[] { row.getNo_slip(), row.getNIK(), row.getNama(), row.getJabatan(), row.getGaji_pokok(), row.getTunjangan(), row.getJumlah_kehadiran(), row.getTotal_gaji()});
            }
            jTable1.setModel(table);
        } catch (SQLException ex) {
            Logger.getLogger(frm_gaji.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
