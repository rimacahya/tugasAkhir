    private void cbo_nikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbo_nikActionPerformed
        try {
            Object item = cbo_nik.getSelectedItem();
            if (item != null)
            {   
                String[] karyawan = gajiService.getKaryawan(cbo_nik.getSelectedItem().toString());
                txt_nama.setText(karyawan[0]);
                txt_jabatan.setText(karyawan[1]);
                txt_gaji_pokok.setText(karyawan[2]);
                txt_tunjangan.setText(karyawan[3]);
                txt_jumlah_kehadiran.setText(karyawan[4]);
                int total = Integer.parseInt(karyawan[2]) + (Integer.parseInt(karyawan[3]) * Integer.parseInt(karyawan[4]));
                txt_total_gaji.setText(Integer.toString(total));
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_cbo_nikActionPerformed

    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        model.model_gaji gaji = new model.model_gaji();
        gaji.setNo_slip(Integer.parseInt(txt_no_slip.getText()));
        gaji.setNIK(cbo_nik.getSelectedItem().toString());
        gaji.setGaji_pokok(Integer.parseInt(txt_gaji_pokok.getText()));
        gaji.setJumlah_kehadiran(Integer.parseInt(txt_jumlah_kehadiran.getText()));
        gaji.setTotal_gaji(Integer.parseInt(txt_total_gaji.getText()));
        gaji.setTunjangan(Integer.parseInt(txt_tunjangan.getText()));
        try {
            gajiService.simpan(gaji);
            this.awal();
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
    }//GEN-LAST:event_btn_simpanActionPerformed

